package uk.ac.gla.dcs.tp3.w.algorithm;

public class AdjListNode {

	int weight;
	AdjListNode next;

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public AdjListNode getNext() {
		return next;
	}

	public void setNext(AdjListNode next) {
		this.next = next;
	}

}
